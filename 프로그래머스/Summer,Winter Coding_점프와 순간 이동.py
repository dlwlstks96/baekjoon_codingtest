def solution(n):
    ans = 0
    
    #주어진 n이 홀수이면 마지막 +1이 되어야 하므로 미리 카운트
    if n % 2 != 0:
        ans += 1
    
    #n이 1보다 크면 계속 반으로 나눈다.
    while n > 1:
        n = n // 2
        if n % 2 != 0: #2로 나눈 n이 홀수라면
            ans += 1

    return ans

